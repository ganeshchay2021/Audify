import 'package:flutter/material.dart';

class AppTextStyle {
  static TextStyle headlineTextStyle(
    double size, {
    Color color = Colors.black,
  }) {
    return TextStyle(color: color, fontSize: size);
  }

  static TextStyle lightlineTextStyle(
    double size, {
    Color color = Colors.black,
  }) {
    return TextStyle(color: color, fontSize: size);
  }
}
